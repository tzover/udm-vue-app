<template>
  <h1>Job Details Page</h1>
  <div v-if="job">
    <h2>{{ job.title }}</h2>
    <p>The Job id is {{ id }}</p>
    <p>{{ job.details }}</p>
  </div>
  <div v-else>
    <p>Loading job details...</p>
  </div>
</template>

<script>
export default {
  props: ['id'],
  data() {
    return {
      // id: this.$route.params.id,
      job: null,
    }
  },
  mounted() {
    fetch('http://localhost:8888/jobs/' + this.id)
      .then((res) => res.json())
      .then((data) => (this.job = data))
      .catch((err) => console.log(err.message))
  },
}
</script>

